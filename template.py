#!/usr/bin/env python

#############################################################################

import zlib, base64

#############################################################################

configure_fp = open('configure.sh', "r")

configure = configure_fp.read()

#############################################################################

if __name__ == '__main__':

	#####################################################################

	configure = base64.b64encode(zlib.compress(configure, 9))

	#####################################################################

	i = 1

	TEMPLATE = 'template = \\\n\tb\''

	for c in configure:
		TEMPLATE += '%c' % c

		i += 1

		if i == 65:
			i = 1;

			TEMPLATE += '\' +\\\n\tb\''

	TEMPLATE += '\'\n\n'

	#####################################################################

	print(TEMPLATE)

	#####################################################################

